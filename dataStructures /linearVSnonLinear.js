// Linear data structure:
// - Data is stored in memory in linear or sequential order.
// - It use the pointer.
// - It is easy to implement.
// - Single level is involved.
// - Memory utilization is ineffective.
// - Eg:-array, link list, stack and queue.

// Non-linear data structure:
// - vData is stored randomly in memory.
// - It does not use pointer.
// - It is comparatively difficult to implement.
// - Multiple level is involved.
// - Memory utilization is effective.
// - Eg:-tree and graph.